package Business.WorkQueue;

/**
 *
 * @author preranaj
 */
public class OncologistAppointment {
    
    String oncologistName;
    String lastName;
    String status;
    int oncologistID;
    String userName;
    String userID;
    String Time;
    String Date;

    public String getOncologistName() {
        return oncologistName;
    }

    public void setOncologistName(String oncologistName) {
        this.oncologistName = oncologistName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getOncologistID() {
        return oncologistID;
    }

    public void setOncologistID(int oncologistID) {
        this.oncologistID = oncologistID;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getTime() {
        return Time;
    }

    public void setTime(String Time) {
        this.Time = Time;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String Date) {
        this.Date = Date;
    }
    
    
    
}
