    private void populateTable() {
        CancerHospitalsDirectory  cdd = ecosystem.getChd();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);
        for (CancerHospitals customer : cdd.getA()) {
                    Object[] row = new Object[5];
                    row[0] = customer.getHospitalName();
                    row[1] = customer.getHospitalLocation();                    
                    row[2] = customer.getPhoneNumber();
                    row[3] = customer.getUserID();
                    row[4] = customer.getPassword();

                    model.addRow(row);
                
            }
        
    }

