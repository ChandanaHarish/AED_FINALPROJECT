    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if( txtName.getText().isEmpty()||txtLastName.getText().isEmpty()|| txtPhNo.getText().isEmpty() || txtPwd.getText().isEmpty() || txtUserID.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "please enter all mandatory fields");
            return;
        }

        if(system.getUserAccountDirectory().checkIfUsernameIsUnique(txtUserID.getText())){
            if(!txtPhNo.getText().matches("[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]"))
            {
                JOptionPane.showMessageDialog(null, " 10 digit phone number");
                txtPhNo.setText("");
                return;
            }

            NCI customer = new NCI(txtName.getText(),txtLastName.getText(), txtPhNo.getText(),  txtUserID.getText(),txtPwd.getText() );
            system.getUserAccountDirectory().addAccount(customer);
            system.getNciDirectory().addgovofficers(customer);
            populateTable();
            txtName.setText("");
            txtLastName.setText("");
            txtPhNo.setText("");
            txtUserID.setText("");
            txtPwd.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Username " + txtUserID.getText() + " already exists !!!, Please try a new one");
        }
    }//GEN-LAST:event_btnAddActionPerformed

